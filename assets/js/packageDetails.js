$(".details-btn").click(function () {
    let packageId = $(this).attr("value");
    setPackageDetails(searchPackageDetails(packageId));
    $("#home-section,#offers-section,#why-choose-section,#transport-section,#feedback-section").css("display", "none");
    $("#package-details-page").css("display", "flex");
});

function searchPackageDetails(packageId) {
    for (let i = 0; i <= packageDetails.length; i++) {
        if (packageDetails[i].packageId === packageId) {
            return i;
        }
    }
}

function setPackageDetails(i) {
    $("#location-img-1").css({
        "background": `url(${packageDetails[i].locationPhotos.image1})`,
        "backgroundPosition": "center",
        "backgroundSize": "cover"
    });
    $("#location-img-2").css({
        "background": `url("${packageDetails[i].locationPhotos.image2}")`,
        "backgroundPosition": "center",
        "backgroundSize": "cover"
    });
    $("#location-img-3").css({
        "background": `url("${packageDetails[i].locationPhotos.image3}")`,
        "backgroundPosition": "center",
        "backgroundSize": "cover"
    });
    $("#location-img-4").css({
        "background": `url("${packageDetails[i].locationPhotos.image4}")`,
        "backgroundPosition": "center",
        "backgroundSize": "cover"
    });
    $("#location-img-5").css({
        "background": `url("${packageDetails[i].locationPhotos.image5}")`,
        "backgroundPosition": "center",
        "backgroundSize": "cover"
    });
    $("#package-topic").text(packageDetails[i].packageTopic);
    for (let itinerary in packageDetails[i].itinerary) {
        $("#itinerary>div").append(
            `<div class="itinerary-detail">
                <p>${packageDetails[i].itinerary[itinerary].day} :</p>
                <p>Day ${packageDetails[i].itinerary[itinerary].fromTo}</p>
                <p>${packageDetails[i].itinerary[itinerary].description}</p>
            </div>`
        );
    }
    $("#package-page-package-type>p").text(packageDetails[i].tourDetails.PackageType);
    $("#package-page-package-duration>p").text(packageDetails[i].tourDetails.PackageDuration);
    $("#package-page-package-location>p").text(packageDetails[i].tourDetails.Locations);
    $("#package-page-package-people>p").text(packageDetails[i].tourDetails.peopleCount);
    $("#price>p").text(packageDetails[i].price);
}
